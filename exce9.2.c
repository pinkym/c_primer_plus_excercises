#include <stdio.h>
#include <string.h>
#include <stdlib.h>

void chline(char * ch, int i, int j);

int main(){
	char ch[200];
	printf("please give a string:\n");
	gets(ch);
	printf("string is %s\n",ch);
	chline(ch, 2, 5);
	
	return 0;
}

void chline(char * ch, int i, int j){
	if(strlen(ch) < j){
		printf("string is not so long!\n");
		exit(-1);
	}
	for(int k = 1; k <= j; k++){
		if(k < i){
			continue;
		}
		putchar(ch[k]);
	}
}
